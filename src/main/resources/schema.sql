DROP TABLE IF EXISTS users, requests, items, bookings, comments;

CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  description VARCHAR(1024) NOT NULL,
  requestor_id bigint REFERENCES users(id) ON UPDATE RESTRICT ON DELETE CASCADE NOT NULL,
  created TIMESTAMP WITHOUT TIME ZONE
);

CREATE TABLE IF NOT EXISTS items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(1024) NOT NULL,
  is_available boolean NOT NULL,
  owner_id bigint REFERENCES users(id) ON UPDATE RESTRICT ON DELETE CASCADE NOT NULL,
  request_id bigint REFERENCES requests(id) ON UPDATE RESTRICT ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS bookings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  start_date TIMESTAMP WITHOUT TIME ZONE,
  end_date TIMESTAMP WITHOUT TIME ZONE,
  item_id bigint REFERENCES items(id) ON UPDATE RESTRICT ON DELETE CASCADE NOT NULL,
  booker_id bigint REFERENCES users(id) ON UPDATE RESTRICT ON DELETE CASCADE NOT NULL,
  status VARCHAR(16) NOT NULL
);

CREATE TABLE IF NOT EXISTS comments (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY NOT NULL,
  text VARCHAR(512) NOT NULL,
  item_id bigint REFERENCES items(id) ON UPDATE RESTRICT ON DELETE CASCADE NOT NULL,
  author_id bigint REFERENCES users(id) ON UPDATE RESTRICT ON DELETE CASCADE NOT NULL,
  created TIMESTAMP WITHOUT TIME ZONE
);